/** Übersetzung abrufen mit Fallback auf Deutsch */
export function t(lang: SupportedLang, key: TranslationKey): string {
  return TRANSLATIONS[lang]?.[key] ?? TRANSLATIONS["de"][key] ?? key;
}

/** Gespeicherte Sprache aus localStorage laden */
export function getStoredLang(): SupportedLang {
  const stored = localStorage.getItem("simplapos_ui_lang");
  if (stored && LANGUAGES.some(l => l.code === stored)) return stored as SupportedLang;
  return "de";
}

/** Sprache in localStorage speichern */
export function setStoredLang(lang: SupportedLang): void {
  localStorage.setItem("simplapos_ui_lang", lang);
}
